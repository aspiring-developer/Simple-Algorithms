// Sum All Numbers in a Range
function sumAll(arr) {
  let sum = 0;
  arr = arr.sort(function (a, b) { return b - a });
  for (let i = arr[1]; i <= arr[0]; i++) {
    sum += i;
  }
  return sum;
}
console.log(sumAll([1, 4])); // [1,4] should return 10
