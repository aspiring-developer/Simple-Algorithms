// FINDING NEXT POSITIVE INTEGER
function findNextPositiveInt(arr) {
  let result = 0;
  let increment = 0;
  while (result === 0) {
    increment++;
    if (arr.indexOf(increment) === -1) {
      result = increment;
    }
  }
  return result;
}
