// FINDING MAX OCCURRING CHARACTER
function maxCharCount(str) {
  let result = {};
  for (let i = 0; i < str.length; i++) {
    if (result[str[i]] > 0) {
      result[str[i]]++;
    } else {
      result[str[i]] = 1;
    }
  }
  return result;
}
